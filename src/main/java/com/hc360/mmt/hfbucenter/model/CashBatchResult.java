public class CashBatchResult {
	private String cashBachResult;
	private String ableMoney;
	private String createTime;
	private String followNumber;

	public String getCashBachResult() {
		return cashBachResult;
	}

	public void setCashBachResult(String cashBachResult) {
		this.cashBachResult = cashBachResult;
	}

	public String getAbleMoney() {
		return ableMoney;
	}

	public void setAbleMoney(String ableMoney) {
		this.ableMoney = ableMoney;
	}

	public String getCreateTime() {
		return createTime;
	}

	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}

	public String getFollowNumber() {
		return followNumber;
	}

	public void setFollowNumber(String followNumber) {
		this.followNumber = followNumber;
	}
}
